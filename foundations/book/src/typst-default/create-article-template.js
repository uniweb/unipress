/**
 * createArticleTemplate({ trim, typography, structure }) → Typst template string.
 *
 * The `article` genre: a single-column, continuous-prose document on a
 * paper size (A4 by default, or Letter), with symmetric single-sided
 * margins, a centered page number, an inline title block (no separate
 * title page), and clean unnumbered section headings. It's the "nicely
 * typeset paper" counterpart to the chaptered book template in
 * create-template.js — the natural shape for turning a folder of notes or
 * docs into one pleasant, shareable PDF.
 *
 * Differences from the book template (create-template.js):
 *   - paper trim (defaults to a4) with symmetric, single-sided margins
 *   - centered page number; no running chapter header
 *   - headings are section headings, not chapter openers — no per-H1 page
 *     breaks, no chapter numbering, no smallcaps running head
 *   - no cover, title page, or copyright page; title/author/date ride in
 *     an inline block at the top of the first page (self-suppressing)
 *   - optional table of contents (structure.toc)
 *
 * Body copy, fonts, and code blocks reuse the same helpers as the book
 * genre (renderFontSet / renderRawRules) so prose renders identically.
 */
import { resolveTrim, resolveTypography, resolveStructure } from './presets.js'
import { renderFontSet, renderRawRules } from './create-template.js'

export function createArticleTemplate({ trim, typography, structure } = {}) {
  // Article defaults to a4 when the document doesn't pick a paper size.
  const t = resolveTrim(trim || 'a4')
  const ty = resolveTypography(typography)
  const st = resolveStructure(structure)
  const fontSet = renderFontSet(ty)
  // A paper has no table of contents by default — it's opt-in via
  // structure.toc (the book genre defaults it on, but an article doesn't).
  const toc = structure?.toc === true ? renderArticleToc(st.tocDepth) : ''

  // Sections are unnumbered by default. structure.numberHeadings: true
  // turns on "1", "1.1", "1.1.1" numbers. The number is rendered inside the
  // custom show rule via counter(heading) + numbering() — the recursion-safe
  // idiom (it.body alone drops the number that #set heading assigns).
  const numbered = st.numberHeadings === true
  const headingNumbering = numbered ? '"1.1.1"' : 'none'
  const num = numbered
    ? '#numbering(it.numbering, ..counter(heading).at(it.location()))#h(0.5em)'
    : ''

  return String.raw`// article.typ — generated by @uniweb/book

#let template(meta: (:), doc) = [
  // ── Document metadata ───────────────────────────────────────────────
  #set document(
    title: meta.at("title", default: ""),
    author: meta.at("author", default: ""),
  )

  // ── Page geometry (paper size, symmetric single-sided margins) ───────
  #set page(
    width: ${t.width},
    height: ${t.height},
    margin: (
      x: ${t.margins.inside},
      top: ${t.margins.top},
      bottom: ${t.margins.bottom},
    ),
    numbering: "1",
  )

  // ── Typography ──────────────────────────────────────────────────────
  #set text(
    size: ${ty.bodySize},
    lang: meta.at("language", default: "en"),
    hyphenate: true,
    costs: (orphan: 100%, widow: 100%),
  )${fontSet}
  #set par(
    justify: true,
    leading: ${ty.leading},
    first-line-indent: ${ty.firstLineIndent},
  )

${renderRawRules(ty)}

  // Section headings — no chapter openers or page breaks. Numbering is
  // controlled by structure.numberHeadings (off by default).
  #set heading(numbering: ${headingNumbering})
  #show heading.where(level: 1): it => block(above: 1.5em, below: 0.7em)[
    #text(size: 16pt, weight: "bold")[${num}#it.body]
  ]
  #show heading.where(level: 2): it => block(above: 1.2em, below: 0.6em)[
    #text(size: 13pt, weight: "bold")[${num}#it.body]
  ]
  #show heading.where(level: 3): it => block(above: 1em, below: 0.5em)[
    #text(size: 11.5pt, weight: "bold", style: "italic")[${num}#it.body]
  ]

  #show figure.caption: it => align(left, text(size: 9pt, it))

  // ── Title block (inline; renders nothing when no title is set) ──────
  #if meta.at("title", default: "") != "" [
    #align(center)[
      #text(size: 20pt, weight: "bold")[#meta.title]
      #if meta.at("subtitle", default: "") != "" [
        #v(0.4em)
        #text(size: 13pt, style: "italic", fill: luma(60))[#meta.subtitle]
      ]
      #if meta.at("author", default: "") != "" [
        #v(0.8em)
        #text(size: 12pt)[#meta.author]
      ]
      #if meta.at("date", default: "") != "" [
        #v(0.3em)
        #text(size: 10pt, fill: luma(90))[#meta.date]
      ]
    ]
    #v(1.8em)
  ]
${toc}  #doc
]
`
}

function renderArticleToc(depth) {
  return `  // ── Table of contents ───────────────────────────────────────────────
  #outline(indent: auto, depth: ${depth})
  #v(1.2em)

`
}
