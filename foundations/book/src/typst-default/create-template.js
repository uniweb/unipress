/**
 * createTemplate({ trim, typography, structure, labels, language, covers })
 *   → Typst template string.
 *
 * Returns a full template.typ parameterised on:
 *   - trim         physical page size + margins (preset or overrides)
 *   - typography   body size, leading, indent, body/heading fonts
 *   - structure    title page / copyright page / TOC / front-matter pagination
 *   - labels       localized built-in strings ('by', etc.) — from labels.js
 *   - language     BCP-47 primary tag used for the label default table
 *   - covers       { front, back } — URLs/paths to full-bleed cover images
 *
 * Back-compat: `createTemplate()` with no args returns the exact same
 * bytes the static `./template` subpath has always exported.
 */
import {
  resolveTrim,
  resolveTypography,
  resolveStructure,
} from './presets.js'
import { resolveLabels } from './labels.js'

export function createTemplate({
  trim,
  typography,
  structure,
  labels,
  language,
  covers,
} = {}) {
  const t = resolveTrim(trim)
  const ty = resolveTypography(typography)
  const st = resolveStructure(structure)
  const lb = resolveLabels({ language, overrides: labels })
  const cv = covers || {}

  const fontSet = renderFontSet(ty)

  // Each block, when non-empty, ends with "\n\n" (last line's newline + one
  // blank line). The template literal leaves a single blank line between
  // the figure-caption show rule and `${sections}`, so concatenating blocks
  // with clean trailing blank lines keeps spacing consistent regardless of
  // which structural pieces are enabled.
  const sections =
    renderFrontCover(cv.front) +
    renderFrontMatterNumbering(st.frontMatterNumbering) +
    renderTitlePage(st.titlePage, lb) +
    renderCopyrightPage(st.copyrightPage) +
    renderToc(st.toc, st.tocDepth) +
    renderBodySection(st.frontMatterNumbering) +
    renderBackCover(cv.back)

  return String.raw`// template.typ — generated by @uniweb/book

#let template(meta: (:), doc) = [
  // ── Document metadata ───────────────────────────────────────────────
  #set document(
    title: meta.at("title", default: ""),
    author: meta.at("author", default: ""),
  )

  // ── Page geometry ──────────────────────────────────────────────────
  #set page(
    width: ${t.width},
    height: ${t.height},
    margin: (
      inside: ${t.margins.inside},
      outside: ${t.margins.outside},
      top: ${t.margins.top},
      bottom: ${t.margins.bottom},
    ),
    header: context {
      let current-page = here().page()
      let all-chapters = query(heading.where(level: 1))
      let on-opener = all-chapters
        .filter(h => h.location().page() == current-page)
        .len() > 0
      if on-opener {
        []
      } else {
        let pg = counter(page).get().first()
        let chapters = query(heading.where(level: 1).before(here()))
        let current-chapter = if chapters.len() > 0 {
          chapters.last().body
        } else {
          ""
        }
        if calc.odd(pg) {
          align(right, smallcaps(current-chapter))
        } else {
          align(left, smallcaps(meta.at("title", default: "")))
        }
      }
    },
    footer: context {
      let pg = counter(page).get().first()
      if calc.odd(pg) {
        align(right, counter(page).display("1"))
      } else {
        align(left, counter(page).display("1"))
      }
    },
  )

  // ── Typography ──────────────────────────────────────────────────────
  #set text(
    size: ${ty.bodySize},
    lang: meta.at("language", default: "en"),
    hyphenate: true,
    costs: (orphan: 100%, widow: 100%),
  )${fontSet}
  #set par(
    justify: true,
    leading: ${ty.leading},
    first-line-indent: ${ty.firstLineIndent},
  )

${renderRawRules(ty)}

  #set heading(numbering: "1.")

  #show heading.where(level: 2): it => {
    block(above: 1.6em, below: 0.9em)[
      #text(size: 14pt, weight: "bold")[#it.body]
    ]
  }
  #show heading.where(level: 3): it => {
    block(above: 1.2em, below: 0.7em)[
      #text(size: 12pt, weight: "bold")[#it.body]
    ]
  }

  #show figure.caption: it => align(left, text(size: 9pt, it))

${sections}]
`
}

// ─── Building blocks ─────────────────────────────────────────────────
//
// Every renderXxx returns either '' (disabled / no-op) or a block
// terminated with "\n\n" (final content line + one blank line). Blocks
// start at two-space indent, matching template body indentation.

function renderRawRules(ty) {
  const relief = (ty.codeMarginRelief || '0pt').toString().trim()
  const hasRelief = relief !== '' && relief !== '0' && relief !== '0pt'

  const inline = `  #show raw.where(block: false): set text(size: ${ty.codeInlineSize})`

  if (hasRelief) {
    // With margin relief, we switch the block rule from `set` style to
    // `show … => …` because the block needs to be wrapped in pad() with
    // a negative x value so it extends beyond the body text column.
    return [
      `  #show raw.where(block: true): it => pad(x: -${relief}, block(`,
      `    fill: luma(240),`,
      `    radius: 4pt,`,
      `    inset: (x: 12pt, y: 12pt),`,
      `    width: 100%,`,
      `    text(size: ${ty.codeBlockSize}, it),`,
      `  ))`,
      inline,
    ].join('\n')
  }

  // Default path — preserves the bytes of the pre-parameterisation template.
  return [
    `  #show raw.where(block: true): set block(`,
    `    fill: luma(240),`,
    `    radius: 4pt,`,
    `    inset: (x: 12pt, y: 12pt),`,
    `    width: 100%,`,
    `  )`,
    `  #show raw.where(block: true): set text(size: ${ty.codeBlockSize})`,
    inline,
  ].join('\n')
}

function renderFontSet(ty) {
  const parts = []
  if (ty.bodyFont) {
    parts.push(`  #set text(font: ${typstFontValue(ty.bodyFont)})`)
  }
  if (ty.headingFont) {
    parts.push(
      `  #show heading: set text(font: ${typstFontValue(ty.headingFont)})`,
    )
  }
  if (ty.codeFont) {
    // Applies to both block and inline raw.
    parts.push(`  #show raw: set text(font: ${typstFontValue(ty.codeFont)})`)
  }
  if (!parts.length) return ''
  return '\n' + parts.join('\n')
}

function typstFontValue(names) {
  if (names.length === 1) return `"${escapeString(names[0])}"`
  const quoted = names.map((n) => `"${escapeString(n)}"`).join(', ')
  return `(${quoted})`
}

function renderFrontMatterNumbering(mode) {
  // 'none' (default) keeps the pre-parameterisation behavior — the title
  // and copyright pages each set `numbering: none` locally and nothing
  // else is numbered until the body resets to "1".
  // 'roman' and 'arabic' set a global front-matter numbering style so the
  // TOC picks it up; the body block resets as appropriate.
  if (mode === 'roman') {
    return `  // ── Front-matter numbering ─────────────────────────────────────────\n  #set page(numbering: "i")\n\n`
  }
  if (mode === 'arabic') {
    return `  // ── Front-matter numbering ─────────────────────────────────────────\n  #set page(numbering: "1")\n\n`
  }
  return ''
}

function renderTitlePage(enabled, labels) {
  if (!enabled) return ''
  const byLabel = escapeTypstText(labels.by)
  return `  // ── Title page ──────────────────────────────────────────────────────
  #pagebreak(to: "even")
  #page(header: none, footer: none, numbering: none)[
    #v(3in)
    #align(center, text(size: 24pt, weight: "bold")[#meta.at("title", default: "")])
    #if meta.at("subtitle", default: "") != "" [
      #v(0.6em)
      #align(
        center,
        text(size: 13pt, style: "italic", fill: luma(60))[#meta.subtitle],
      )
    ]
    #v(1.2em)
    #align(center, text(size: 13pt)[${byLabel} #meta.at("author", default: "")])
  ]
  #pagebreak()

`
}

function renderCopyrightPage(enabled) {
  if (!enabled) return ''
  // The `\\ ISBN` and `\\ #meta.publisher` must emit literal backslashes.
  // Using String.raw here so the \\ stays a double-backslash in the
  // generated string (Typst renders a line break from `\`).
  return String.raw`  // ── Copyright / colophon page ───────────────────────────────────────
  #page(header: none, footer: none, numbering: none)[
    #v(1fr)
    #align(center, [
      #text(size: 9pt, fill: luma(90))[
        #meta.at("rights", default: "")
        #if meta.at("isbn", default: "") != "" [ \\ ISBN #meta.isbn ]
        #if meta.at("publisher", default: "") != "" [ \\ #meta.publisher ]
      ]
    ])
  ]
  #pagebreak(to: "even")

`
}

function renderToc(enabled, depth) {
  if (!enabled) return ''
  return `  // ── Table of contents ───────────────────────────────────────────────
  #set outline.entry(fill: repeat([.], gap: 0.25em))
  #outline(
    indent: auto,
    depth: meta.at("toc_depth", default: ${depth}),
  )
  #pagebreak()

`
}

function renderBodySection(frontMatterMode) {
  // 'arabic' shares the counter between front matter and body — no reset.
  // 'none' and 'roman' both restart the body at page 1 arabic.
  const reset =
    frontMatterMode === 'arabic'
      ? `  #set page(numbering: "1")`
      : `  #set page(numbering: "1")\n  #counter(page).update(1)`

  return `  // ── Body ────────────────────────────────────────────────────────────
${reset}

  #doc
`
}

function renderFrontCover(url) {
  if (!url) return ''
  return `  // ── Front cover ─────────────────────────────────────────────────────
  #page(
    margin: (x: 0cm, y: 0cm),
    header: none,
    footer: none,
    numbering: none,
  )[#image("${escapeString(url)}", width: 100%, height: 100%)]

`
}

function renderBackCover(url) {
  if (!url) return ''
  // Leading newline gives the blank-line separator after `#doc`; no
  // trailing blank line because the template's closing `]` follows.
  return `
  // ── Back cover ──────────────────────────────────────────────────────
  #page(
    margin: (x: 0cm, y: 0cm),
    header: none,
    footer: none,
    numbering: none,
  )[#image("${escapeString(url)}", width: 100%, height: 100%)]
`
}

// ─── Escaping helpers ─────────────────────────────────────────────────

function escapeString(s) {
  // Typst string literals follow C-style escaping for \ and ".
  return String(s).replace(/\\/g, '\\\\').replace(/"/g, '\\"')
}

function escapeTypstText(s) {
  // Content-mode escaping — characters that would otherwise be Typst syntax.
  return String(s).replace(/[\\#\*_\[\]]/g, (c) => '\\' + c)
}
